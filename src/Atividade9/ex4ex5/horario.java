package Atividade9.ex4ex5;

public class horario {
    int segundo;
    int minuto;
    int hora;


    public horario(int hora, int minuto, int segundo){
        this.hora=hora;
        this.minuto = minuto;
        this.segundo =segundo;





    }

    public String toString(){
        return hora+ ":"+ minuto + ";" + segundo;



    }



}
